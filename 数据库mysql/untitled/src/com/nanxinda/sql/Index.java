package com.nanxinda.sql;
@SuppressWarnings({"all"})
public class Index {
    /*
    索引概述：
    索引是帮助MySQL高校获取数据的数据结构（有序），在数据之外，数据库系统还维护着满足特定查找算法的
    数据结构，这些数据结构以某种方式（指向）数据，这样就可以在这些数据结构上实现高级查找算法，这种数据结构
    就是索引
     */
    /**
     * ==================================================================================
     * MySQL 索引优缺点对比表
     * ==================================================================================
     * |   维度         |                优势 (Pros)               |          劣势 (Cons)          |
     * | :------------ | :--------------------------------------- | :--------------------------- |
     * |  数据检索      | 极大提高检索效率，降低数据库 IO 成本          |                              |
     * |               |                                          |                              |
     * |  性能优化      | 降低数据排序成本，减轻 CPU 消耗              |                              |
     * |               |                                          |                              |
     * |  存储空间      |                                          | 索引列需占用额外的物理存储空间   |
     * |               |                                          |                              |
     * |  数据维护      |                                          | 降低 INSERT/UPDATE/DELETE 效率|
     * ==================================================================================
     * 💡 总结：索引是“空间换时间”的典型设计，要在查询频率和写入压力之间寻找平衡点。
     */
    /**
     * ====================================================================================================
     * MySQL 索引结构及描述汇总表
     * ====================================================================================================
     * |    索引结构           |                                 描述                                     |
     * | :------------------ | :----------------------------------------------------------------------- |
     * |  B+Tree 索引        | 最常见的索引类型，大部分引擎都支持 B+ 树索引。                                     |
     * |                     |                                                                          |
     * |  Hash 索引          | 底层数据结构是用哈希表实现的，只有精确匹配索引列的查询才有效，不支持范围查询。             |
     * |                     |                                                                          |
     * |  R-tree (空间索引)   | 空间索引是 MyISAM 引擎的一个特殊索引类型，主要用于地理空间数据类型，通常使用较少。         |
     * |                     |                                                                          |
     * |  Full-text (全文索引)| 是一种通过建立倒排索引，快速匹配文档的方式。类似于 Lucene, Solr, ES。              |
     * ====================================================================================================
     * 💡 核心笔记：MySQL 的索引是在【存储引擎层】实现的，这意味着不同的存储引擎支持的索引结构可能不同。
     * 例如：InnoDB 默认使用 B+Tree，而某些场景下会使用自适应 Hash 索引。
     */
    /**
     * =================================================================================================
     * MySQL 存储引擎与索引结构支持矩阵
     * =================================================================================================
     * |    索引结构      |      InnoDB      |      MyISAM      |      Memory      |
     * | :-------------- | :---------------- | :---------------- | :---------------- |
     * |  B+Tree 索引     |       支持        |       支持        |       支持        |
     * |                  |                  |                  |                  |
     * |  Hash 索引       |      不支持       |      不支持       |       支持        |
     * |                  |                  |                  |                  |
     * |  R-tree 索引     |      不支持       |       支持        |      不支持       |
     * |                  |                  |                  |                  |
     * |  Full-text 索引  |  5.6版本之后支持   |       支持        |      不支持       |
     * =================================================================================================
     * 💡 关键结论：
     * 1. B+Tree 是唯一被所有主流引擎支持的索引结构，也是我们的首选。
     * 2. 如果你需要使用 Hash 索引来获得极高的点查询性能，通常会考虑使用 Memory 引擎。
     * 3. 虽然 InnoDB 现在也支持全文索引(Full-text)，但大规模搜索通常还是建议配合 Elasticsearch 使用。
     */
     /// 索引结构
     /// 1.MySQL索引数据结构对经典的B+Tree进行了优化，在原B+Tree的基础上，增加一个指向相邻叶子节点的链表指针
     /// 就形成了带有顺序指针的B+Tree，提高区间访问的性能
     /// 2.Hash：哈希索引就是采取一定的hash算法，将键值换算成新的hash值，映射到对应的槽位上，然后存储在hash表中
     /// 如果两个（或多个）键值，映射到相同的槽位上，他们就产生hash冲突（也称为hash碰撞），可以通过链表来解决

     //Hash索引特点：
     //1.hash索引只能用于对等比较（=，in），不支持范围查询(between,>,<,...)
     //2.无法利用索引完成排序操作
     //3.查询效率高，通常只需要一个索引就可以，效率通常要高于B+treesuoyin
     //存储引擎支持
     //在MySQL中，支持hash索引的是Memory引擎，而InnoDB中具有自适应hash功能，hash索引是存储引擎根据B+Tree
     //索引在指定条件下自动构建的。

     /**
      * 数据库索引分类总结
      * +----------+------------------------------------------------+--------------------------+----------+
      * |   分类   |                      含义                      |           特点           |  关键字  |
      * +----------+------------------------------------------------+--------------------------+----------+
      * | 主键索引 | 针对于表中主键创建的索引                      | 默认自动创建，只能有一个 | PRIMARY  |
      * | 唯一索引 | 避免同一个表中某数据列中的值重复               | 可以有多个               | UNIQUE   |
      * | 常规索引 | 快速定位特定数据                            | 可以有多个               | (None)   |
      * | 全文索引 | 查找文本中的关键词，而不是比较索引中的值       | 可以有多个               | FULLTEXT |
      * +----------+------------------------------------------------+--------------------------+----------+
      */
     //回表查询：先根据二级索引找到对应的主键值，再根据聚集索引找到这行的行数据

}
