    //具体操作：MySQL 会申请一个新的空页。将原页中 50% 的数据移动到新页中。更新父节点的指针，让它指向这个新页。
    //页合并：当删除一记录时，实际上记录并没有被物理删除，只是记录被标记（flaged）为删除并且它的空间变得允许被其他记录声明使。
    //当页中删除的记录达到MERGE_THRESHOLD（默认为页的50%），InnoDB会开始寻找最靠近的页（前或后）看看是否可以将两个页合并以优化空间使用。
    /// 页分裂和页合并本质对应B+树中的添加和删除

    //主键设计原则：
    //1.满足业务需求的情况下，尽量降低主键的长度。（二级索引会存储主键，如果主键过长，会增加回表查询的随机I/O）
    //2.插数据时，尽量选择顺序插，选择使AUTO_INCREMENT增主键。
    // （可以避免无序插入的页分裂，但是无法解决唯一性，在多个分布式的数据库中会冲突）
    //3.尽量不要使UUID做主键或者是其他然主键，如身份证号。
    // （UUID就是为了唯一性设计的，但是它是无序的，所以就会面临页分裂，除此之后他的长度很长，不利于查找性能）
    //4.业务操作时，避免对主键的修改。
