    //主键优化
    //数据组织方式：在InnoDB存储引擎种，表数据都是根据主键顺序组织存放的，这种存储方式的表称为索引组织表
    //+-----------------------------------------------------------+
    //|  Tablespace (表空间: 最大的容器，对应磁盘上的 .ibd 文件)      |
    //|  +-----------------------------------------------------+  |
    //|  |  Segment (段: 分为数据段、索引段、回滚段等)              |
    //|  |  +-----------------------------------------------+  |  |
    //|  |  |  Extent (区: 默认 1M，包含 64 个连续的页)        |  |  |
    //|  |  |  +-----------------------------------------+  |  |  |
    //|  |  |  |  Page (页: 默认 16K，磁盘管理的最小单位)    |  |  |  |
    //|  |  |  |  +-----------------------------------+  |  |  |  |
    //|  |  |  |  |  Row (行: 最终存放数据的地方)        |  |  |  |  |
    //|  |  |  |  |  [Trx id | Roll pointer | Data...] |  |  |  |  |
    //|  |  |  |  +-----------------------------------+  |  |  |  |
    //|  |  |  +-----------------------------------------+  |  |  |
    //|  |  +-----------------------------------------------+  |  |
    //|  +-----------------------------------------------------+  |
    //+-----------------------------------------------------------+
