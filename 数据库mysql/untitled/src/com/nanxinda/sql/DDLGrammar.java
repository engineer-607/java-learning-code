package com.nanxinda.sql;

public class DDLGrammar {
    /*
    DDL-数据库操作
    1.查询：
     查询所有数据库:show databases
     查询当前数据库:select database();
    2.创建:create database[if not exists]数据库名[default charset 字符集][collate 排序规则]
    3.删除:drop database[if exists]数据库名;
    4.使用：use 数据库名
     */
    /*
    DDL-表操作-查询
    1.查询当前数据库所有表：show tables;
    2.查询表结构：desc 表名;
    3.查询指定表的建表语句：show create table 表名;
     */
    /*
    create table 表名(
       字段1 int [comment 字段注释1],
       字段2 varchar(50) [comment 字段注释2],
       字段3 int comment [comment 字段注释3],
       字段4 varchar(1) [comment 字段注释4]
       )[comment 字段注释5];
       eg.
       create table tb_user(
    -> id int comment '编号',
    -> name varchar(50) comment '姓名',
    -> age int comment '年龄',
    -> gendar varchar(1) comment '性别')comment '用户表格';
     */
/*
============================================================================
MySQL 数据类型参考表
============================================================================
| 分类 | 类型              | 大小       | 有符号范围                           | 无符号范围                            | 描述              |
|------|-------------------|------------|--------------------------------------|---------------------------------------|-------------------|
| 3    | TINYINT           | 1 byte     | (-128, 127)                          | (0, 255)                              | 小整数值          |
| 4    | SMALLINT          | 2 bytes    | (-32768, 32767)                      | (0, 65535)                            | 大整数值          |
| 5    | MEDIUMINT         | 3 bytes    | (-8388608, 8388607)                  | (0, 16777215)                         | 大整数值          |
| 6    | INT/INTEGER       | 4 bytes    | (-2147483648, 2147483647)            | (0, 4294967295)                       | 大整数值          |
| 7    | BIGINT            | 8 bytes    | (-2^63, 2^63-1)                      | (0, 2^64-1)                           | 大整数值          |
| 8    | FLOAT             | 4 bytes    | (-3.402823E+38, 3.402823E+38)        | 0 和 (1.175494153E-38, 3.402823E+38) | 单精度浮点数值    |
| 9    | DOUBLE            | 8 bytes    | (-1.7976931348623157E+308, 1.7976931348623157E+308) | 0 和 (2.2250738585072014E-308, 1.7976931348623157E+308) | 双精度浮点数值 |
| 10   | DECIMAL           | 依赖 M 和 D | 依赖 M 和 D                          | 依赖 M 和 D                           | 小数值(精确定点数)|
============================================================================
说明：
- M: 精度（总位数）
- D: 标度（小数点后的位数）
- 无符号类型不含负数，正数范围扩大一倍
*/

/*
============================================================================
MySQL 字符串/文本类型参考表
============================================================================
| 分类        | 大小                      | 描述                         |
|-------------|---------------------------|------------------------------|
| CHAR        | 0-255 bytes               | 定长字符串                   |
| VARCHAR     | 0-65535 bytes             | 变长字符串                   |
| TINYBLOB    | 0-255 bytes               | 不超过255个字符的二进制数据  |
| TINYTEXT    | 0-255 bytes               | 短文本字符串                 |
| BLOB        | 0-65535 bytes             | 二进制形式的长文本数据       |
| TEXT        | 0-65535 bytes             | 长文本数据                   |
| MEDIUMBLOB  | 0-16777215 bytes          | 二进制形式的中等长度文本数据 |
| MEDIUMTEXT  | 0-16777215 bytes          | 中等长度文本数据             |
| LONGBLOB    | 0-4294967295 bytes (4GB)  | 二进制形式的极大文本数据     |
| LONGTEXT    | 0-4294967295 bytes (4GB)  | 极大文本数据                 |
============================================================================
说明：
- CHAR 为定长，VARCHAR 为变长,eg.char(10), 如果只输入一个字符，也占据10个字节，其他用空格填充（性能好）；varchar(10),如果只输入一个字节，便只占据一个字节（性能差）
- BLOB 系列存二进制数据，TEXT 系列存字符串
*/
/*
MySQL 日期与时间类型对照表
=====================================
分类        | 类型       | 大小(字节) | 范围                                         | 格式                   | 描述
------------|------------|------------|----------------------------------------------|------------------------|-------------------------------
DATE        | DATE       | 3          | 1000-01-01 至 9999-12-31                     | YYYY-MM-DD             | 日期值
TIME        | TIME       | 3          | -838:59:59 至 838:59:59                      | HH:MM:SS               | 时间值或持续时间
YEAR        | YEAR       | 1          | 1901 至 2155                                 | YYYY                   | 年份值
DATETIME    | DATETIME   | 8          | 1000-01-01 00:00:00 至 9999-12-31 23:59:59   | YYYY-MM-DD HH:MM:SS    | 混合日期和时间值
TIMESTAMP   | TIMESTAMP  | 4          | 1970-01-01 00:00:00 至 2038-01-19 03:14:07   | YYYY-MM-DD HH:MM:SS    | 混合日期和时间值，时间戳
*/
    /*
    DDL-表操作-修改
    修改数据类型 alter table 表名 modify 字段名 新数据类型（长度）
    修改字段名和字段类型 alter table 表名 change 旧字段名 新字段名 类型（长度）[comment 注释][约束]
    删除字段 alter table 表名 drop 字段名
    修改表名 alter table 表名 rename to 新表名
     */
    /*
    DDL-表操作-删除
    删除表 drop table [if exists] 表名;
    删除指定表，并重新创建该表 truncate table 表名
     */

}
