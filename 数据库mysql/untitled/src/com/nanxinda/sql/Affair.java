package com.nanxinda.sql;
@SuppressWarnings("all")
public class Affair {
    /*
    事务：是一组操作的集合，它是一个不可分割的工作单位，事务会把所有的操作作为一个整体一起向系统提交或撤销
    操作请求，即这些操作要么同时成功，要么同时失败
    查看/设置事务提交方式
    方式一：
    select @@autocommit//查看事务的提交方式，是否是自动提交，如果为1就是自动提交，如果为0就是手动提交
    set @@autocommit=0;
    提交事务
    commit
    回滚事务
    rollback
    方式二：
    开启事务：
    start transaction 或 begin;
    提交事务：
    commit;
    回滚事务：
    rollback;
     */
    /*
    事务四大特性(ACID):
    1.原子性：事务是不可分割的最小操作单元，要么全部成功，要么全部失败
    2.一致性：事务完成时，必须使所有的数据都保持一致状态
    3.隔离性：数据库系统提供的隔离机制，保证事务在不受外部并发操作影响的独立环境下运行
    4.持久性：事务一旦提交或回滚，它对数据库种的数据的改变就是永久的
     */
    /*
    并发事务问题：
    1.脏读：一个事务读到另外一个事务还没有提交的数据
    2.不可重复读：一个事务先后读取同一条记录，但两次读取的数据不同，称为不可重复读
    3.一个事务按照条件查询数据时，没有对应的数据行，但是在插入数据时，又发现这行数据已经存在，好像出现了
    “幻影”
     */
    /*
    事务隔离级别：
    /*
    * MySQL 事务隔离级别 vs 并发问题对照表
    * --------------------------------------------------------------------------
    * 隔离级别 (Isolation Level)   | 脏读 (Dirty Read) | 不可重复读 (Non-Repeatable) | 幻读 (Phantom)
    * --------------------------------------------------------------------------
    * Read uncommitted (读未提交)  |      √           |            √               |      √
    * Read committed (读已提交)    |      ×           |            √               |      √
    * Repeatable Read (可重复读)   |      ×           |            ×               |      √
    * Serializable (串行化)        |      ×           |            ×               |      ×
    * --------------------------------------------------------------------------
    * 注：
    * 1. "√" 表示可能发生，"×" 表示已解决。
    * 2. Repeatable Read 是 MySQL InnoDB 引擎的默认隔离级别。
    */
    //查看事务隔离级别
    //select@@transaction_isolation
    //设置事务隔离级别
    //set[session | global] transaction isolation level{read uncommitted | read committed | repeatable commited | serializable}
    //session 是对当前对话客户端窗口有效，global是对所有客户端对话窗口有效
    /**
     *1.read uncommited由于可以读未提交的，所以造成脏读
     *在 Java 中，就像两个线程同时操作一个非线程安全的变量，没有任何 volatile 或 synchronized 保护。
     *2.read commited 由于可以读已提交的，所以导致不可重复读，即别的事务对数据库的修改能够读出来
     * RC级别在每次执行 SQL 语句时都会重新生成一个一致性视图（Read View）。
     * 这意味着它追求的是“绝对实时”，但牺牲了事务内部的“稳定性”。
     *3.repeatable read 由于只读该事务开始之前的数据库数据，但是实时反馈现在数据库的状态,
     * 就导致别的控制台在数据库中添加成功，而当前控制台无法检测到，
     * 认为该数据库某个数据仍然为空，进行添加的却又发现已经存在这种矛盾的幻读
     * 在 MySQL 中，这种情况通常通过间隙锁 (Gap Lock) 来解决，即在查询时就把可能插入新数据的“间隙”锁住，不让别人插。
     * 4.而serializable只允许一个事务对当前数据库进行操作，有点像java中的锁，只有一个线程能进行操作
     * 完全等同于 Java 中的 synchronized 关键字或全局锁，同一时间只允许一个事务（线程）操作当前数据范围。
     */
}
