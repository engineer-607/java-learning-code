package com.nanxinda.net;

public class Net {
    /*
    网络通信
    1.概念：两台设备之间通过网络实现数据传输
    2.网络通信：将数据通过网络从一台设备传输到另一台设备
    3.在java.net包下提供一系列的类或者接口，完成网络通信
     */
    //网络
    //1.概念：两台或者多台设备通过一定物理设备连接起来构成了网络
    //2.根据网络的覆盖范围不同，对网络进分类：
    //*局域网：覆盖范围最小，仅仅覆盖一个教室或一个机房
    //*城域网：覆盖范围较大，可以覆盖一个城市
    //*广域网：覆盖范围最大，可以覆盖全国，甚至全球，万维网是广域网的代表

    //ip地址
    //1.概念：用于唯一标识网络中的每台计算机/主机
    //2.查看ip地址：ipconfig
    //3.ip表示：对于iPv4可以用4个字节（32位）表示
    //4.ip地址的表示形式：点分十进制 xx.xx.xx.xx
    //5.每一个字节表示的十进制的范围0-255
    //6.ip地址的组成=网络地址+主机地址，比如192.168.16.69（网络地址：192.168.16，主机地址：69）
    //7.iPv6是互联网工程任务组成设计的用于替代iPv4的下一代IP协议
    //8.iPv4最大的问题在于网络地址资源有限，严重制约互联网的应用和发展。iPv6的使用，不仅能解决网络
    //地址资源数量的问题，而且也解决多种接入设备连入互联网的障碍
    // 1. IP地址由网络号 + 主机号组成
// 2. 分为A、B、C、D、E五类，以及特殊地址

// ---------- A类地址 ----------
// 格式：|0|网络号(7位)|主机号(24位)|
// 范围：0.0.0.0 到 127.255.255.255
// 特点：首位为0，网络号占7位，主机号占24位

// ---------- B类地址 ----------
// 格式：|1|0|网络号(14位)|主机号(16位)|
// 范围：128.0.0.0 到 191.255.255.255
// 特点：前两位为10，网络号占14位，主机号占16位

// ---------- C类地址 ----------
// 格式：|1|1|0|网络号(21位)|主机号(8位)|
// 范围：192.0.0.0 到 223.255.255.255
// 特点：前三位为110，网络号占21位，主机号占8位

// ---------- D类地址（多播地址）----------
// 格式：|1|1|1|0|多播组号(28位)|
// 范围：224.0.0.0 到 239.255.255.255
// 特点：前四位为1110，用于多播组

// ---------- E类地址（保留地址）----------
// 格式：|1|1|1|1|0|留待后用(27位)|
// 范围：240.0.0.0 到 247.255.255.255
// 特点：前五位为11110，保留用于实验或将来使用

// ---------- 特殊地址 ----------
// 127.0.0.1 : 表示本机地址（回环地址）
// 常用于本地网络测试

// 总结：
// A类：0.0.0.0 ~ 127.255.255.255
// B类：128.0.0.0 ~ 191.255.255.255
// C类：192.0.0.0 ~ 223.255.255.255
// D类：224.0.0.0 ~ 239.255.255.255（多播）
// E类：240.0.0.0 ~ 247.255.255.255（保留）
// 127.0.0.1 是本地回环地址

    //域名
    //1.概念：将ip地址映射成域名（HTTP协议）
    //2.eg.www.baidu.com
    //3.优点：为了方便记忆，解决记住ip的困难

    //端口号
    //1.概念：用于表示计算机上某个特定的网络程序
    //2.表示形式：以整数形式，范围0-65535【2个字节表示端口】
    //3.0-1024已经被占用，比如ssh 22，ftp 21，smtp 25，http 80
    //（在网络开发中，不要使用0-1024的端口）
    //4.常见的网络程序端口号：
    //*tomcat：8080
    //*mysql：3306
    //*oracle：1521
    //*sqlserver：1433

// ==================== 访问流程概览 ====================
//
//                     百度域名
//                 www.baidu.com
//                       │
//                       │ 访问
//                       ▼
//    ┌──────────────────────────────────────┐
//    │             电脑/浏览器               │
//    └──────────────────────────────────────┘
//                       │
//                       │ 请求数据
//                       ▼
//    ┌──────────────────────────────────────┐
//    │         百度主机IP：110.242.68.3      │
//    └──────────────────────────────────────┘
//                       │
//                       │ 返回数据
//                       ▼
//    ┌──────────────────────────────────────┐
//    │             电脑/浏览器               │
//    └──────────────────────────────────────┘

// ==================== 端口与服务映射 ====================
//
//  IP地址 + 端口号  →  特定服务
//
//  110.242.68.3:80      →   网站服务（HTTP）
//  110.242.68.3:25      →   邮件服务（SMTP）
//  110.242.68.3:8080    →   Tomcat服务（Java应用服务器）

// ==================== 详细通信模型 ====================

/**
 * ┌─────────────┐     ① 域名解析      ┌─────────────┐
 * │             │  www.baidu.com ───→ │             │
 * │   浏览器    │                     │   DNS服务器  │
 * │             │ ←────────────────── │             │
 * └─────────────┘     ② 返回IP        └─────────────┘
 *        │
 *        │ ③ 发起请求（IP + 端口）
 *        ▼
 * ┌─────────────────────────────────────────────────┐
 * │              百度服务器 110.242.68.3             │
 * ├─────────────┬─────────────┬─────────────────────┤
 * │   端口80    │   端口25    │      端口8080        │
 * │  网站服务   │  邮件服务   │    Tomcat服务        │
 * │  (HTTP)     │  (SMTP)     │   (Java Web)        │
 * └─────────────┴─────────────┴─────────────────────┘
 *        │
 *        │ ④ 返回响应数据
 *        ▼
 * ┌─────────────┐
 * │   浏览器    │
 * │  渲染页面   │
 * └─────────────┘
 */

// ==================== 关键概念说明 ====================

// 1. 域名访问流程：
//    www.baidu.com (域名) → DNS解析 → 110.242.68.3 (IP地址)

// 2. IP地址标识网络上的设备：
//    110.242.68.3 是百度服务器的主机IP

// 3. 端口号标识设备上的特定服务：
//    - 端口80  ：HTTP网站服务（默认Web服务）
//    - 端口25  ：SMTP邮件服务
//    - 端口8080：Tomcat应用服务器（常用于Java Web应用）

// 4. 访问格式：
//    IP地址:端口号  例如：110.242.68.3:80

// 5. 浏览器通过 "IP + 端口" 定位到目标服务器上的具体服务程序
//    服务器处理请求后，通过相同的路径返回数据给浏览器

// ==================== 总结 ====================
// 用户通过域名访问网站
// DNS将域名解析为IP地址
// 浏览器通过IP地址 + 端口号连接到服务器上的特定服务
// 服务器处理请求并返回数据
// 浏览器渲染数据显示给用户

    //网络通信协议
    //协议（tcp/ip）
    //TCP/IP：传输控制协议/因特网互联协议，又称网络通讯协议，这个协议是Internet最基本的协议
    //（数据的组织形式其实就是一种协议，就像人类之间交流的语言就是一种协议）

/**

     * ┌───────────────────┬───────────────────┬─────────────────────────┐
             * │    OSI模型        │   TCP/IP模型      │   TCP/IP各层对应协议    │
            * │   (7层)           │   (4层)           │                         │
            * ├───────────────────┼───────────────────┼─────────────────────────┤
            * │                   │                   │                         │
            * │    ▲ 应用层       │                   │  ▲ HTTP                 │
            * │    │              │                   │  │ FTP                  │
            * │    │ 表示层       │    应用层         │  │ Telnet               │
            * │    │              │                   │  │ DNS                  │
            * │    │ 会话层       │                   │  │ ...                  │
            * │                   │                   │                         │
            * ├───────────────────┼───────────────────┼─────────────────────────┤
            * │                   │                   │                         │
            * │     传输层        │    传输层         │  ▲ TCP                  │
            * │                   │    (TCP)          │  │ UDP                  │
            * │                   │                   │  │ ...                  │
            * ├───────────────────┼───────────────────┼─────────────────────────┤
            * │                   │                   │                         │
            * │     网络层        │    网络层         │  ▲ IP                   │
            * │                   │    (IP)           │  │ ICMP                 │
            * │                   │                   │  │ ARP                  │
            * │                   │                   │  │ ...                  │
            * ├───────────────────┼───────────────────┼─────────────────────────┤
            * │                   │                   │                         │
            * │   ▼ 数据链路层    │                   │                         │
            * │      │            │  物理+数据链路层  │        Link             │
            * │   ▼ 物理层        │                   │      (物理/链路层协议)   │
            * │                   │                   │                         │
            * └───────────────────┴───────────────────┴─────────────────────────┘
 */

//UDP：
    //1.将数据、源、目的封装成数据包，不需要建立连接
    //2.每个数据包的大小限制在64K内，不适合传输大量数据
    //3.因无需链接，故是不可靠的
    //4.发送数据结束时无序释放资源（因为不是面向连接的），速度快
// ==================== UDP协议类比：发短信 ====================

/**
 * ┌─────────────────────────────────────────────────────────┐
 * │                         UDP                              │
 * │              （用户数据报协议）                            │
 * │                 无连接、不可靠但快速                       │
 * └─────────────────────────────────────────────────────────┘
 *                            │
 *                            ▼
 * ┌─────────────────────────────────────────────────────────┐
 * │                     类比：发短信                          │
 * ├─────────────────────────────────────────────────────────┤
 * │                                                          │
 * │   tom                          kim                       │
 * │    │                            │                        │
 * │    │  发短信："hello"            │                        │
 * │    │  to: 15566667777  ──────→  │                        │
 * │    │                            │                        │
 * │    │  （不管对方收到没有）        │                        │
 * │    │                            │                        │
 * │    │  可能存在的问题：            │                        │
 * │    │  ❌ 对方手机关机             │                        │
 * │    │  ❌ 对方没信号               │                        │
 * │    │  ❌ 短信延迟/丢失            │                        │
 * │    │  ❌ 短信顺序错乱              │                        │
 * │    │                            │                        │
 * │    │  （继续发下一条，不管结果）    │                        │
 * │    │                            │                        │
 * └─────────────────────────────────────────────────────────┘
 */
//TCP协议：传输控制协议
    //1.使用TCP协议前，须建立TCP连接，形成传输通道
    //2.传输前，采用“三次握手”方式，是可靠的
    //3.TCP协议进行通信的两个应用进程：客户端、服务端
    //4.在连接中可进行大数据量的传输
    //5.传输完毕，需释放已建立的连接，效率低
// ==================== TCP协议类比：打电话 ====================

/**
 * ┌─────────────────────────────────────────────────────────┐
 * │                         TCP                              │
 * │                  （传输控制协议）                          │
 * │                   面向连接、可靠传输                       │
 * └─────────────────────────────────────────────────────────┘
 *                            │
 *                            ▼
 * ┌─────────────────────────────────────────────────────────┐
 * │                     类比：打电话                          │
 * ├─────────────────────────────────────────────────────────┤
 * │                                                          │
 * │   tom                          kim                       │
 * │    │                            │                        │
 * │    │  ① "你在听吗？"            │                        │
 * │    │ ───────────────────────→  │                        │
 * │    │                            │                        │
 * │    │  ② "我在听"                │                        │
 * │    │ ←───────────────────────  │                        │
 * │    │                            │                        │
 * │    │  ③ "下面我给你说一个事情，认真听。"│                    │
 * │    │ ───────────────────────→  │                        │
 * │    │                            │                        │
 * │    │      开始说正事。。。        │                        │
 * │    │ ───────────────────────→  │                        │
 * │    │                            │                        │
 * │    │  ④ "听清楚了，我重复一遍"    │                        │
 * │    │ ←───────────────────────  │                        │
 * │    │                            │                        │
 * │    │  ⑤ "好的，没错"             │                        │
 * │    │ ───────────────────────→  │                        │
 * │    │                            │                        │
 * └─────────────────────────────────────────────────────────┘
 */

}
