class GrandFather{
      String name="中头爷爷";
      String hobby="旅游";
      double salary=2000;
}
class Father extends GrandFather{
    String name="小头爸爸";
    int age=39;
    private double salary=30000;
    public double getSalary(){
        return salary;
    }


}
class Son extends Father{
    String name="大头儿子";
}