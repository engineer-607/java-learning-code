package com.nanxinda.class_.String_;

public class String01 {
    public static void main(String[] args) {
        /*
        1）String对象用于保存字符串，也就是一组字符序列
        String实现接口Serializable，说明String可以串行化（将对象转化为可以存储或者传输的数据流）
        String实现接口Comparable，说明String对象可以比较
        2）字符串常量对象是用双引号括起来的字符序列，eg."你好"
        3）字符串的字符使用Unicode字符编码，一个字符占两个字节
        4）字符串常用的构造器
              String 类有很多构造器，构造器的重载
              String s1 = new String();
              String s2 = new String(String original);
              String s3 = new String(char[] a);
              String s4 = new String(char[] a, int startIndex, int count);
              String s5 = new String(byte[] b);//常用于网络传输
        5）String是final类，不能被其他的类继承
        6）String有属性 private final char values[];用于存放字符串内容
        7）private final char values[]中final保证这个数组引用不能发生改变
        理论上数组内容可以改变，但是由于String的设计（属性设为private，不提供
        修改方法）实际上无法被外部修改
         */
        /*
        创建String对象的两种方式：
        1）直接赋值String s1 = "hsp";
        2）调用构造器 String s2 = new String("hsp");
        方式一：先从常量池查看是否存在"hsp"数据空间，如果有，直接指向
        如果没有则重新创建，然后指向，s1最终指向的是常量池的空间地址

        内存布局：
┌─────────────────────────────────────────────────────┐
│                   栈 Stack                          │
├─────────────────────────────────────────────────────┤
│ main() 方法栈帧                                     │
│   s1 ──────┐                                        │
└────────────┼────────────────────────────────────────┘
             │ 引用指向常量池中的"hsp"
             ↓
┌─────────────────────────────────────────────────────┐
│              方法区/常量池                         │
├─────────────────────────────────────────────────────┤
│ 字符串常量池                                        │
│   "hsp"  <──────┐  (地址假设：0x99)                 │
└─────────────────┼───────────────────────────────────┘
                  │ 直接引用
                  │
┌─────────────────┼───────────────────────────────────┐
│                   堆 Heap                          │
│              (此方式不在堆中创建对象)               │
└─────────────────────────────────────────────────────┘
要点：
1. s1直接指向常量池中的"hsp"
2. 不创建新的String对象
3. 如果常量池已有"hsp"，则直接复用

        方式二：先在堆中创建空间，里面为何value属性，指向常量池的hsp空间。
        如果常量池没有"hsp"，重新创建，如果有，直接通过value指向。最终指向
        的是堆中的空间地址

        内存布局：

 * 代码：String s2 = new String("hsp");
 * 内存布局（现代JVM，JDK 8+）：
 * ┌─────────────────────────────────────────────────────────────┐
 * │                        栈 Stack                             │
 * ├─────────────────────────────────────────────────────────────┤
 * │ main() 方法栈帧                                              │
 * │                                                             │
 * │  局部变量表：                                                │
 * │  ┌────────────┬─────────────┬─────────────────┐            │
 * │  │  变量名    │   类型      │     值          │            │
 * │  ├────────────┼─────────────┼─────────────────┤            │
 * │  │    s2      │  String     │   0x1001        │ → 引用     │
 * │  └────────────┴─────────────┴─────────────────┘            │
 * │       ↑                                                    │
 * │       │ 指向堆中的String对象                                │
 * └───────┼─────────────────────────────────────────────────────┘
 *         │
 *         │
 *         ↓
 * ┌─────────────────────────────────────────────────────────────┐
 * │                        堆 Heap                              │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  String对象 (地址：0x1001)                                   │
              │
 * │  ├─────────────────────────────────────────┤               │
 * │  │ 字段：value (char[]引用)                │ → 0x2002      │
 * │  │  - private final char value[]           │               │
 * │  ├─────────────────────────────────────────┤               │
 * │                    │                                       │
 * │                    │ value字段指向char数组对象             │
 * │                    ↓                                       │
 * │  char[]数组对象 (地址：0x2002)                              │
 * │  ┌─────────────────────────────────────────┐               │
 * │  │ 对象头                                  │               │
 * │  ├─────────────────────────────────────────┤               │
 * │  │ 类型指针 (指向char[]类元数据)            │               │
 * │  ├─────────────────────────────────────────┤               │
 * │  │ 数组长度：3                             │               │
 * │  ├─────────────────────────────────────────┤               │
 * │  │ 元素[0]：'h'                           │               │
 * │  │ 元素[1]：'s'                           │               │
 * │  │ 元素[2]：'p'                           │               │
 * │  └─────────────────────────────────────────┘               │
 * │                                                             │
 * ├─────────────────────────────────────────────────────────────┤
 * │              字符串常量池 (也在堆中，特殊区域)              │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  "hsp" 字面量缓存 (地址：0x3003)                            │
 * │  ┌─────────────────────────────────────────┐               │
 * │  │ value: → 0x2002 (指向上面的char数组)    │               │
 * │  └─────────────────────────────────────────┘               │
 * │                                                        │
 * │        当字面量第一次出现时，会在这里创建缓存              │
 * │        后续使用时直接从这里获取                           │
 * └────────────────────────────────────────────────────────────┘
 *
 *          重要：String对象的value指向的char数组
 *          与常量池缓存的是同一个数组对象！
         */
        //解析intern()
        //当调用intern方法时，如果池已经包含一个等于String对象的字符串（用equals(Object)方法确定）
        //则返回池中的字符串。否则将此String对象添加到池中，并返回此String对象的引用
        String a ="jack";//a指向常量池的"jack"
        String b = new String("jack");//b指向堆中对象，对象中的value数组指向"jack"
        System.out.println(a==b.intern());//b.intern()返回的是常量池中"jack"的引用

    }
}
