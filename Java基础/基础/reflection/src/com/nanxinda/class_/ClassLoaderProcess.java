package com.nanxinda.class_;

public class ClassLoaderProcess {
/**
 * com.Cat.java ──javac编译──→ com.Cat.class
 *                                    │
 *                                    ↓
 * ┌─────────────────────────────────────────────────────────┐
 * │ 1. 加载 (Loading)                                        │
 * │ 【任务】                                                  │
 * │   • 将类的class文件读入内存                               │
 * │   • 为之创建一个java.lang.Class对象                       │
 * │   • 此过程由类加载器完成                                  │
 * └─────────────────────┬───────────────────────────────────┘
 *                       │
 *                       ↓
 * ┌─────────────────────────────────────────────────────────┐
 * │ 2. 连接 (Linking)                                        │
 * │                                                         │
 * │  ├─ 2.1 验证 (Verification)                             │
 * │  │    【任务】验证字节码安全性、合法性                    │
 * │  │     • 文件格式验证                                    │
 * │  │     • 元数据验证                                      │
 * │  │     • 字节码验证                                      │
 * │  │     • 符号引用验证                                    │
 * │  │                                                      │
 * │  ├─ 2.2 准备 (Preparation)                              │
 * │  │    【任务】为类变量分配内存并设置默认值                │
 * │  │     • static变量分配内存                              │
 * │  │     • 设置默认初始值 (0, null, false)                │
 * │  │                                                      │
 * │  └─ 2.3 解析 (Resolution)                               │
 * │       【任务】将类的二进制数据合并到JRE中                 │
 * │        • 符号引用 → 直接引用                             │
 * │        • 解析类、接口、字段、方法等                       │
 * └─────────────────────┬───────────────────────────────────┘
 *                       │
 *                       ↓
 * ┌─────────────────────────────────────────────────────────┐
 * │ 3. 初始化 (Initialization)                               │
 * │ 【任务】JVM负责对类进行初始化                             │
 * │   • 这里主要是指静态成员                                  │
 * │   • 执行静态代码块                                       │
 * │   • 静态变量赋初始值（程序员指定值）                      │
 * └─────────────────────┬───────────────────────────────────┘
 *                       │
 *                       ↓
 *
 * ──────────────── 类加载后内存情况 ────────────────
 *
 *   方法区                                   堆区
 * ┌──────────────┐                     ┌──────────────┐
 * │ 类的字节码    │                     │ Class对象    │
 * │ 二进制数据    │ ──指向(引用)──→      │ (java.lang. │
 * │ (结构信息)    │     指向            │ Class实例)   │
 * └──────────────┘                     └──────────────┘
 *                                             ↑
 *                                             │
 *                                      ┌──────────────┐
 *                                      │ 静态变量      │
 *                                      │ (JDK7+移至堆)│
 *                                      └──────────────┘
 */

    /// 加载阶段：
    ///JVM在该阶段的主要目的是将字节码从不同的数据源（可能是class文件，也可能是jar包，甚至网络）
    /// 转化为二进制字节流加载到内存之中，并生成一个代表该类的java.lang.class对象

    /// 连接阶段-验证：
    /// 1.目的是为了确保Class文件的字节流中包含的信息符合当前虚拟机的要求，并且不会危害虚拟机自身的安全
    /// 2.包括：文件验证格式（是否以cafe babe（十六进制）开头）、元数据验证、字节码验证和符号引用验证
    /// 3.可以考虑使用-Xverify:none参数来关闭大部分的类验证措施，缩短虚拟机加载的时间

    /// 连接阶段-准备：
    /// 1.JVM会在该阶段对静态变量，分配内存并默认初始化（例如，int类型为0，long类型为0L，boolean类型
    /// 为false等等）。这些变量所使用的内存都将在方法区中进行分配

    /// 连接阶段-解析：
    /// 1.虚拟机将常量池内的符号引用变为直接引用的过程
    /// 2.符号引用：本质上就是一个字符串（代码里的变量名、函数名）
    ////（可以理解为“书名”，只要知道其函数名，就可以知道位置）
    /// 3.直接引用：一个具体的内存地址或偏移量

    /// Initialization（初始化）
    /// 1.在初始化阶段，才真正开始执行类中定义的Java程序代码（这个阶段是执行<clinit>()方法的过程）
    /// 2.<clinit>()方法是由编译器按语句在源文件中出现的顺序，依次自动类中的所有静态变量的赋值动作和
    /// 静态代码块中的语句并进行合并
    /// 3.虚拟机会保证一个类的<clinit>()方法在多线程环境中被正确地加锁、同步，如果多个线程同时去初始化
    /// 一个类，那么只会有一个线程去执行这个类的<clinit>()方法，其他线程都需要阻塞等待，直到活动线程执行
    /// <clinit>()方法完毕
      public static void main(String[] args) {
          //1.加载B类，并生成B的class对象
          //2.链接 num = 0
          //3.初始化阶段
          //  依次自动收集类中的所有静态变量的赋值动作和静态代码块中的语句
          /*
              clinit(){
                          System.out.println("B静态代码块被执行");
                          num = 300;
                          num = 100;
              }
           */
          System.out.println(B.num);//100,直接使用静态属性也会导致类加载
          //加载类的时候有同步机制
          /*
              protected Class<?> loadClass(String name, boolean resolve)
        throws ClassNotFoundException
    {
    //正因为有该机制才能保证在某个类在内存中只有一份Class对象
        synchronized (getClassLoadingLock(name)) {
        .......
            }
            if (resolve) {
                resolveClass(c);
            }
            return c;
        }
    }
           */

      }
}
class A{
    //属性-成员变量-字段
    //1.n1 是实例属性，不是静态变量，因此在准备阶段，是不会分配内存的
    //2.n2 是静态变量，分配内存并初始化为默认值0（不是20）
    //3.n3 是static final为常量，它和静态变量不一样，一旦赋值就值不变为30
    public int n1 = 10;
    public static int n2 = 20;
    public static final int n3 = 30;

}
class B{
    static {
        System.out.println("B静态代码块被执行");
        num = 300;
    }
    static int num = 100;

    public B() {
    }
}
