package com.nanxinda.service;

import org.springframework.transaction.annotation.Propagation;
import org.springframework.transaction.annotation.Transactional;

public interface LogService {
    @Transactional(propagation = Propagation.REQUIRES_NEW)
    public void log(String out,String in,Double money);
    /// 要求事务管理员给自己开启一个全新的事务(REQUIRES_NEW)
    /**
     * =========================================================================================
     * 事务传播行为 (Transaction Propagation)
     * =========================================================================================
     * | 传播属性 (Propagation) | 事务管理员 (Active T1?) | 事务协调员 (Behavior/Action)           |
     * =========================================================================================
     * | REQUIRED (默认)         | 开启T1                 | 加入T1 (Joins existing T1)             |
     * |                         | 无                     | 新建T2 (Creates new T2)                |
     * -----------------------------------------------------------------------------------------
     * | REQUIRES_NEW            | 开启T1                 | 新建T2 (Suspends T1, creates new T2)   |
     * |                         | 无                     | 新建T2 (Creates new T2)                |
     * -----------------------------------------------------------------------------------------
     * | SUPPORTS                | 开启T1                 | 加入T1 (Runs within T1)                |
     * |                         | 无                     | 无     (Runs non-transactionally)      |
     * -----------------------------------------------------------------------------------------
     * | NOT_SUPPORTED           | 开启T1                 | 无     (Suspends T1, runs without T)   |
     * |                         | 无                     | 无     (Runs non-transactionally)      |
     * -----------------------------------------------------------------------------------------
     * | MANDATORY               | 开启T1                 | 加入T1 (Joins existing T1)             |
     * |                         | 无                     | ERROR  (Throws exception if no T1)     |
     * -----------------------------------------------------------------------------------------
     * | NEVER                   | 开启T1                 | ERROR  (Throws exception if T1 exists) |
     * |                         | 无                     | 无     (Runs non-transactionally)      |
     * -----------------------------------------------------------------------------------------
     * | NESTED                  | 设置 savePoint，一旦事务回滚，事务将回滚到 savePoint 处，       |
     * |                         | 交由客户端响应提交/回滚                                         |
     * =========================================================================================
     */

//    | 传播行为属性        |        外部有事务时    | 外部无事务时 | 适用场景举例 |
//     =================             =========     =========    ========
//    | **REQUIRED**      | 加入当前事务           | 新建事务    | 绝大多数普通业务，如订单创建、扣减库存。 |
//    | **REQUIRES_NEW**  | 挂起当前事务，新建事务   | 新建事务    | 独立日志记录。不论核心业务成功与否，日志必须记录成功。 |
//    | **SUPPORTS**      | 加入当前事务           | 非事务运行   | 查询方法。如果别人在事务中调我，我能读到未提交数据；如果没事务，我就正常查询。 |
//    | **NOT_SUPPORTED** | 挂起当前事务，非事务运行 | 非事务运行   | 耗时较长的非数据库操作（如发送大文件、调用外系统API），避免长期占用事务连接。 |
//    | **MANDATORY**     |  加入当前事务          | 抛出异常     | 必须由上层控制核心流程的子操作，不允许被单独直接调用。 |
//    | **NEVER**         | 抛出异常              | 非事务运行   | 严格限制不能有事务的操作（例如某些长连接的批量同步）。 |
//    | **NESTED**        | 在当前事务中嵌套保存点   | 新建事务    | 大业务中的分支流程。比如充值大礼包，核心充值成功，但礼包发放失败时，只回滚礼包，不回滚充值。 |
}
